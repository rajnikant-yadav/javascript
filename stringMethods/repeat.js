// repeat()
// In JavaScript, the repeat() method is a built-in method for strings that allows you to create a new string by repeating the original string a specified number of times. The syntax for the repeat() method is as follows:
// string.repeat(count)

//repeat()
var string = "Welcome to Edureka";
console.log(string.repeat(2));
//output: Welcome to EdurekaWelcome to Edureka

let originalString = "Hello ";
let repeatedString = originalString.repeat(3);

console.log(repeatedString);
// Output: "Hello Hello Hello "
