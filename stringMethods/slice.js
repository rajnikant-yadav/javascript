// In JavaScript, the slice() method is a built-in method for strings and arrays. It is used to extract a portion of a string or an array and returns a new string or array without modifying the original. The syntax for the slice() method is similar for both strings and arrays:
//  string.slice(startIndex, endIndex)


const str = 'Mozilla';

console.log(str.slice(1, 3));
// expected output: "oz"

console.log(str.slice(2));
// Output : zilla